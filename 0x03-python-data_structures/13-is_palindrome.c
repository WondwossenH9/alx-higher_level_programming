/**
*add_nodeint - adds a new node at index zero of a listint_t list
*@head: head of list
*@n: int to add
*Return: address of the new element, or NULL if fail
*/
listint_t *add_nodeint(listint_t **head, const int n)
{
	listint_t *new;

	new = malloc(sizeof(listint_t));
	if (new == NULL)
		return (NULL);
	new->n = n;
	new->next = *head;
	*head = new;
	return (new);
}
/**
*is_palindrome - checks if the single linked list is palindrome
*@head: head of list
*Return: 1 if it is palindrome otherwise 0
*/
